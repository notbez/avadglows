const catalogContainer = document.getElementById("catalog-container");
const buttons = document.querySelectorAll(".filter-btn");

function renderCatalog(category = "all") {
    catalogContainer.innerHTML = "";
    const filteredCatalog =
        category === "all"
            ? catalog
            : catalog.filter((item) => item.category === category);

    filteredCatalog.forEach((item) => {
        catalogContainer.innerHTML += `
            <div class="product" data-category="${item.category}">
                <img src="${item.img}" alt="${
            item.name
        }" style="width: 40%; height="auto"">
                <h3>${item.name}</h3>
                <p>${item.price.toLocaleString()} ₽</p>
                <button onclick="addToCart(${
                    item.id
                })">Добавить в корзину</button>
                <button class="details-btn" data-id="${
                    item.id
                }">Подробнее</button>
                <p class="product-description" id="desc-${
                    item.id
                }" style="display: none;">${item.description}</p>
            </div>
        `;
    });

    document.querySelectorAll(".details-btn").forEach((button) => {
        button.addEventListener("click", (event) => {
            const productId = event.target.dataset.id;
            const description = document.getElementById(`desc-${productId}`);
            description.style.display =
                description.style.display === "none" ? "block" : "none";
        });
    });
}

buttons.forEach((button) => {
    button.addEventListener("click", () => {
        const category = button.getAttribute("data-category");
        renderCatalog(category);
    });
});

function addToCart(id) {
    let cart = JSON.parse(localStorage.getItem("cart")) || [];
    const product = catalog.find((item) => item.id === id);
    cart.push(product);
    localStorage.setItem("cart", JSON.stringify(cart));
}

renderCatalog();
